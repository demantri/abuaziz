<script>
    new window.JustValidate('.js-form', {
        rules: {
            name: {
                required: true
            },
            password: {
                required: true
            }
        },
        messages: {
          name: {
            required: 'Form Tidak Boleh Kosong'
          },
          password: 'Form Tidak Boleh Kosong'
        },

        submitHandler: function (form, values, ajax) {
            $.ajax({
              url: "<?= site_url('auth/login'); ?>",
              method: "POST",
              data: $('#form-login').serialize(),
              dataType: "JSON",
              success: function(response) {
                if(response.status =="benar") {
                    $.toast({
                        heading: 'Info',
                        text: 'Selamat Datang!',
                        position: 'top-right',
                        showHideTransition: 'slide',
                        icon: 'info'
                    });
                    setTimeout(function(){ window.location.href = "<?php echo site_url('dashboard'); ?>"; },2000);
                } else if(response.status =="salah"){
                    $.toast({
                        heading: 'Info',
                        text: 'Username / Password Salah',
                        position: 'top-right',
                        showHideTransition: 'slide',
                        icon: 'warning'
                    });
                } else if(response.status =="blokir"){
                    $.toast({
                        heading: 'Bahaya',
                        text: 'Akun Anda Terblokir!',
                        position: 'top-right',
                        showHideTransition: 'slide',
                        icon: 'error'
                    });
                }
              }
            });
        },
    });
    
